# Learning Data Types #

"""
This is really important to know as they are used in almost everything programming, variables store data types which
all do different things.
"""

# Built-in #
TextType = str
NumberType = int, float, complex
SequenceType = list, tuple, range
MappingType = dict
SetType = set, frozenset
BooleanType = bool
BinaryType = bytes, bytearray, memoryview
NoneType = None

# Setting Data Types and Specific Data Types #
